nomes = ['Davi', 'João', 'Pedro', 'Phillipi', 'Willian']

# para retornar o primeiro elemento da lista
print(nomes[0])

#para retornar todos os elementos da lista
print(nomes[:])
print(nomes)
# para retornar so elemento 2 até o final da lista
print(nomes[2:])

# para retornar o do primeiro elemento até o elemento 3
print(nomes[:3])

# para retornar os elementos entre 2 e 4 (vai mostrar elemento 2 e 3)
print(nomes[2:4])

